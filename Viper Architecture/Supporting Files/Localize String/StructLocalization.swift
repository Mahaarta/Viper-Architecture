        static let nice = "nice"
        static let okay = "okay"
        static let back = "back"
        static let whoops = "whoops"
        static let viperArchitecture = "viper-architecture"
    }
    
    struct Login {
        static let loginTitle = "login-title"
        static let loginDesc = "login-desc"
        static let dontHaveAccount = "dont-have-account"
        static let loginSuccess = "login-success"
        static let loginFailed = "login-failed"
    }
    
    struct Register {
        static let signUp = "signup"
        static let registerDesc = "register-desc"
        static let registerTitle = "register-title"
        static let alreadyHaveAccount = "already-have-account"
    }
    
    struct TextView {
        static let name = "name"
        static let email = "email"
        static let userName = "userName"
        static let password = "password"
    }
    
}
